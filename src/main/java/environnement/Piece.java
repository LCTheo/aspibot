package environnement;

public class Piece {

    private boolean poussiere;
    private boolean bijou;

    public Piece() {
    }

    public boolean isPoussiere() {
        return poussiere;
    }

    public void setPoussiere(boolean poussiere) {
        this.poussiere = poussiere;
    }

    public boolean isBijou() {
        return bijou;
    }

    public void setBijou(boolean bijou) {
        this.bijou = bijou;
    }
}
